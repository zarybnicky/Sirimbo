create or replace function public.on_update_author_announcement() returns trigger
    language plpgsql
    as $$
begin
  if new.author_id is null then
    new.author_id = current_user_id();
  end if;
  return new;
end;
$$;
select verify_function('public.on_update_author_announcement', 'public.announcement');

drop trigger if exists on_update_author_announcement on public.announcement;
create trigger on_update_author_announcement
  before insert or update on public.announcement
  for each row execute function public.on_update_author_announcement();


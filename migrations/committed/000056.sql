-- Drop dependent functions so we can replace the announcement view
-- with a real table while keeping callers intact.
drop function if exists public.archived_announcements();
drop function if exists public.sticky_announcements(boolean);
drop function if exists public.my_announcements(boolean, boolean);
drop function if exists public.archived_announcement_new();
drop function if exists public.sticky_announcement_new();
drop function if exists public.my_announcement_new(boolean);

do $$
begin
  if exists (select 1 from pg_views where viewname = 'announcement') then
    drop view if exists announcement;
  end if;
  if exists (select 1 from pg_Tables where tablename = 'upozorneni') and exists (select 1 from pg_tables where tablename = 'announcement') then
    drop table if exists announcement;
  end if;
end;
$$;

create table if not exists public.announcement (
  id bigint generated by default as identity,
  tenant_id bigint not null default public.current_tenant_id(),
  author_id bigint,
  title text not null,
  body text not null,
  is_locked boolean not null default false,
  is_visible boolean not null default true,
  is_sticky boolean not null default false,
  created_at timestamptz not null default CURRENT_TIMESTAMP,
  updated_at timestamptz,
  scheduled_since timestamptz,
  scheduled_until timestamptz,
  constraint announcement_pkey primary key (id),
  constraint announcement_tenant_id_fkey foreign key (tenant_id) references public.tenant(id) on delete cascade,
  constraint announcement_author_id_fkey foreign key (author_id) references public.users(u_id) on update restrict on delete restrict
);

grant all on table public.announcement to anonymous;

alter table public.announcement enable row level security;

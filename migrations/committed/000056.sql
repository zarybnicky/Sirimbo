  seq text;
  max_id bigint;
begin
  select pg_get_serial_sequence('public.announcement', 'id') into seq;
  if seq is not null then
    select coalesce(max(id), 0) into max_id from public.announcement;
    if max_id = 0 then
      execute format('select setval(%L, 1, false);', seq);
    else
      execute format('select setval(%L, %s, true);', seq, max_id);
    end if;
  end if;
end;
$$;

create or replace function public.archived_announcements() returns setof public.announcement
    language sql stable
    as $$
  select announcement.*
  from public.announcement
  where is_visible = false
     or (scheduled_until is null or scheduled_until >= now())
  order by created_at desc;
$$;
grant all on function public.archived_announcements() to anonymous;

create or replace function public.sticky_announcements(order_by_updated boolean default false) returns setof public.announcement
    language sql stable
    as $$
  select announcement.*
  from public.announcement
  where is_visible = true and is_sticky = true
    and (scheduled_since is null or scheduled_since <= now())
    and (scheduled_until is null or scheduled_until >= now())
  order by
    case when order_by_updated then updated_at else created_at end desc,
    created_at desc;
$$;
grant all on function public.sticky_announcements(order_by_updated boolean) to anonymous;

create or replace function public.my_announcements(archive boolean default false, order_by_updated boolean default false) returns setof public.announcement
    language sql stable
    as $$
  select announcement.*
  from public.announcement
  where is_visible = not archive and is_sticky = false
    and (scheduled_since is null or scheduled_since <= now())
    and (scheduled_until is null or scheduled_until >= now())
  order by
    case when order_by_updated then updated_at else created_at end desc,
    created_at desc;
$$;
grant all on function public.my_announcements(archive boolean, order_by_updated boolean) to anonymous;

begin
  if not exists (
    select 1
    from pg_catalog.pg_type type
    join pg_catalog.pg_namespace namespace on namespace.oid = type.typnamespace
    where namespace.nspname = 'public'
      and type.typname = 'event_registration_source'
  ) then
    create type public.event_registration_source
      as enum ('self', 'manager', 'cohort');
  end if;
end;
$$;


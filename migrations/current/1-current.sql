create table if not exists public.event_series (
  id bigint primary key generated by default as identity,
  tenant_id bigint not null default public.current_tenant_id()
    references public.tenant (id) on update cascade on delete cascade,
  name text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint event_series_tenant_id_id_key unique (tenant_id, id)
);

create or replace trigger _100_timestamps
  before insert or update on public.event_series
  for each row execute function app_private.tg__timestamps();

comment on table public.event_series is E'@behavior -query:resource:list -query:resource:connection -query:resource:single';
comment on constraint event_series_tenant_id_fkey on public.event_series is
  E'@fieldName tenant\n@foreignFieldName eventSeries';

grant all on table public.event_series to anonymous;
grant usage, select on sequence public.event_series_id_seq to anonymous;
alter table public.event_series enable row level security;

alter table public.event_instance
  add column if not exists series_id bigint;
comment on column public.event_instance.series_id is
  'Groups related events without supplying inherited event values.';

alter table public.event_series disable trigger _100_timestamps;
insert into public.event_series (
  id, tenant_id, name, created_at, updated_at
) overriding system value
select event.id, event.tenant_id, event.name,
  coalesce(event.created_at, now()), coalesce(event.updated_at, event.created_at, now())
from public.event event
where exists (
  select 1 from public.event_instance instance where instance.event_id = event.id
)
on conflict (id) do nothing;
alter table public.event_series enable trigger _100_timestamps;

select setval(
  pg_get_serial_sequence('public.event_series', 'id'),
  coalesce((select max(id) from public.event_series), 1),
  exists (select 1 from public.event_series)
);

alter table public.event_instance disable trigger _100_timestamps;
update public.event_instance
set series_id = event_id
where series_id is null and event_id is not null;
alter table public.event_instance enable trigger _100_timestamps;

create index if not exists event_instance_tenant_series_idx
  on public.event_instance (tenant_id, series_id);

alter table public.event_instance
  drop constraint if exists event_instance_series_fkey;
alter table public.event_instance
  add constraint event_instance_series_fkey
  foreign key (tenant_id, series_id)
  references public.event_series (tenant_id, id)
  on update cascade
  on delete set null (series_id)
  not valid;

alter table public.event_instance
  validate constraint event_instance_series_fkey;
comment on constraint event_instance_series_fkey on public.event_instance is
  E'@fieldName series\n@foreignFieldName events';


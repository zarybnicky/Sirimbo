do $$
begin
  if exists (
    select 1 from pg_constraint
    where conname = 'event_instance_registration_target_cohort_id_fkey'
      and conrelid = 'public.event_instance_registration'::regclass
      and confrelid = 'public.event_target_cohort'::regclass
  ) then
    insert into public.event_instance_target_cohort (
      tenant_id, instance_id, cohort_id, created_at, updated_at
    )
    select target.tenant_id, instance.id, target.cohort_id,
      target.created_at, target.updated_at
    from public.event_target_cohort target
    join public.event_instance instance on instance.event_id = target.event_id
    on conflict (instance_id, cohort_id) do nothing;

    alter table public.event_instance_registration
      drop constraint event_instance_registration_target_cohort_id_fkey;

    alter table public.event_instance_registration disable trigger _100_timestamps;
    update public.event_instance_registration registration
    set target_cohort_id = target.cohort_id
    from public.event_target_cohort target
    where target.id = registration.target_cohort_id;
    alter table public.event_instance_registration enable trigger _100_timestamps;

    alter table public.event_instance_registration
      add constraint event_instance_registration_target_cohort_id_fkey
      foreign key (target_cohort_id) references public.cohort(id)
      on update cascade on delete restrict;
  end if;
end;
$$;


create table if not exists public.event_instance_target_cohort (
  id bigint generated by default as identity primary key,
  tenant_id bigint not null default public.current_tenant_id()
    references public.tenant(id) on update cascade on delete cascade,
  instance_id bigint not null
    references public.event_instance(id) on update cascade on delete cascade,
  cohort_id bigint not null
    references public.cohort(id) on update cascade on delete restrict,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (instance_id, cohort_id)
);


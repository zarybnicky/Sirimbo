  disable trigger _500_refresh_manager_person_ids;
insert into public.event_instance_trainer (
  tenant_id, instance_id, person_id, lessons_offered
)
select trainer.tenant_id, instance.id, trainer.person_id, trainer.lessons_offered
from public.event_instance instance
join public.event_trainer trainer on trainer.event_id = instance.event_id
where not exists (
  select 1 from public.event_instance_trainer exact
  where exact.instance_id = instance.id
)
on conflict (instance_id, person_id) do nothing;
alter table public.event_instance_trainer
  enable trigger _500_refresh_manager_person_ids;

